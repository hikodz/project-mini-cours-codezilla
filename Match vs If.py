# Employees and their offices
# a
Cairo = ['Islam Mahfouz', 'Mohamed Mesilhy',
     'Hatem Elmaghraby', 'Kareem Embaby']
# b
Riyadh = ['Mohamed Gouda', 'Ayman Hamed',
     'Seif Ali', 'Adham Wael']
# c
Casablanca = ['Ahmed Ashraf', 'Abd El-Rahman Mahrous',
     'Islam Sheta', 'Mohamed Medhat', 'Mahmoud Nasser']
# d
Dubai= ['Ahmed Alaa', 'Kareem Abd-Elmeged',
     'Osama Ashraf', 'Mohamed Mostafa', 'Ahmed Bedeir']

#! input user:
enter_coun = input('Enter the name of the office: ')

#! convert list:
convert_list_a = ', '.join(Cairo[:-1])
convert_list_b = ', '.join(Riyadh[:-1])
convert_list_c = ', '.join(Casablanca[:-1])
convert_list_d = ', '.join(Dubai[:-1])

#! use match in data
match enter_coun.title():
    case 'Cairo' | 'Egypt':
        print(f'The employees in {enter_coun} are: {convert_list_a} and {Cairo[-1]}')
    case 'Riyadh' | 'Saudi Arabia' | 'Ksa':
        print(f'The employees in {enter_coun} are: {convert_list_b} and {Riyadh[-1]}')
    case 'Casablanca' | 'Morocco':
        print(f'The employees in {enter_coun} are: {convert_list_c} and {Casablanca[-1]}')
    case 'Dubai' | 'Uae':
        print(f'The employees in {enter_coun} are: {convert_list_d} and {Dubai[-1]}')
    case _:
        print('Not found')        

